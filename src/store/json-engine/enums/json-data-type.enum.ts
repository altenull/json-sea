/**
 * See the [JSON Data Types](http://www.ecma-international.org/ecma-262/7.0/#sec-ecmascript-language-types)
 */
export enum JsonDataType {
  Object = 'object', // For `Object` node type
  Array = 'array', // For `Array` node type
  String = 'string', // For `Primitive` node type
  Number = 'number', // For `Primitive` node type
  Boolean = 'boolean', // For `Primitive` node type
  Null = 'null', // For `Primitive` node type
}
